# Asignación de valores a variables

nombre = "Juan"

edad = 25

altura = 1.75

es_estudiante = True

# Impresión de variables

print(f"Nombre: {nombre}")

print(f"Edad: {edad}")

print(f"Altura: {altura}")

print(f"Es estudiante: {es_estudiante}")

# Cambio de valores de variables

nombre = "María"

edad = 30

altura = 1.60

es_estudiante = False

# Impresión de variables actualizadas

print(f"Nombre: {nombre}")

print(f"Edad: {edad}")

print(f"Altura: {altura}")

print(f"Es estudiante: {es_estudiante}")
